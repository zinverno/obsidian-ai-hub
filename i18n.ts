import { getLanguage } from "obsidian";

export type AIHubLang = "auto" | "en" | "ru";

let resolved: "en" | "ru" = "ru";

export function setLanguage(l: AIHubLang): void {
  if (l === "auto") {
    const sys = getLanguage();
    resolved = sys && sys.toLowerCase().startsWith("ru") ? "ru" : "en";
  } else {
    resolved = l;
  }
}

export function currentLanguage(): "en" | "ru" {
  return resolved;
}

export function dateLocale(): string {
  return resolved === "ru" ? "ru-RU" : "en-US";
}

/**
 * Все языковые варианты перевода ключа (сам ключ — русский вариант).
 * Для языконезависимых проверок: например, найден ли в тексте заголовок,
 * который могла вставить любая локаль.
 */
export function tAll(key: string): string[] {
  const variants = new Set<string>([key]);
  if (RU[key]) variants.add(RU[key]);
  if (EN[key]) variants.add(EN[key]);
  return [...variants];
}

export function t(key: string, vars?: Record<string, string | number>): string {
  let s: string;
  if (resolved === "ru") {
    s = RU[key] ?? key;
  } else {
    s = EN[key] ?? RU[key] ?? key;
  }
  if (vars) {
    // Один проход по шаблону: подставленные значения не пересканируются,
    // поэтому литеральные "{n}"/"{links}" внутри значений не заменяются
    s = s.replace(/\{(\w+)\}/g, (match, k: string) =>
      Object.prototype.hasOwnProperty.call(vars, k) ? String(vars[k]) : match,
    );
  }
  return s;
}

